bas = float(input("Ingresa la longitud de la base del romboide: "))
alt = float(input("Ingresa la altura del romboide: "))
lad = float(input("Ingresa la longitud de uno de los lados del romboide: "))

area = bas * alt
perimetro = 2 * (bas + lad)

print("El área del romboide es:", area)
print("El perímetro del romboide es:", perimetro)
